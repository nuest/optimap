class SourceApiTests(TestCase):
    def test_source_api_exposes_landing_and_feed_urls(self):
        source = _make_source("Api Source")
        resp = Client().get(f"/api/v1/sources/{source.pk}/")
        self.assertEqual(resp.status_code, 200)
        data = resp.json()
        self.assertEqual(data["slug"], source.slug)
        self.assertTrue(data["landing_page_url"].endswith(f"/in/{source.slug}/"))
        self.assertIn(f"source-{source.slug}.rss", data["feed_georss_url"])
